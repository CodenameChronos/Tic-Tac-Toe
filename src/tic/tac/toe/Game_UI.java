    public void win(String text1, String text2, String text3, String text4, String text5, String text6, String text7, String text8, String t9) {
    // Arreglo para almacenar las marcas en el tablero
    String[] board = {text1, text2, text3, text4, text5, text6, text7, text8, t9};

    // Combinaciones ganadoras (índices del tablero)
    int[][] winningCombinations = {
        {0, 1, 2}, {3, 4, 5}, {6, 7, 8}, // Filas
        {0, 3, 6}, {1, 4, 7}, {2, 5, 8}, // Columnas
        {0, 4, 8}, {2, 4, 6} // Diagonales
    };

    // Verifica si alguno de los jugadores ha ganado
    for (int[] combination : winningCombinations) {
        String marker = board[combination[0]];
        if (!marker.equals("") && board[combination[1]].equals(marker) && board[combination[2]].equals(marker)) {
            JOptionPane.showMessageDialog(null, "¡" + marker + " gana!");
            return; // El juego ha terminado
        }
    }

    // Si no hay ganador y todas las casillas están llenas, es un empate
    boolean isTie = true;
    for (String cell : board) {
        if (cell.equals("")) {
            isTie = false;
            break;
        }
    }

    if (isTie) {
        JOptionPane.showMessageDialog(null, "¡Empate!");
    }
}

    
