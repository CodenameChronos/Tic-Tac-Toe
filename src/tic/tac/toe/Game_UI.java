    private void updateButton(JButton button, int index) {
        if (board[index].isEmpty()) {
            board[index] = (player_turn == 0) ? "X" : "O";
            button.setText(board[index]);
            player_turn = 1 - player_turn; // Alternar entre 0 y 1
            checkForWinner();
        }
    }
    private void checkForWinner() {
        int[][] winningCombinations = {
            {0, 1, 2}, {3, 4, 5}, {6, 7, 8}, // Filas
            {0, 3, 6}, {1, 4, 7}, {2, 5, 8}, // Columnas
            {0, 4, 8}, {2, 4, 6} // Diagonales
        };

        for (int[] combination : winningCombinations) {
            String marker = board[combination[0]];
            if (!marker.isEmpty() && board[combination[1]].equals(marker) && board[combination[2]].equals(marker)) {
                JOptionPane.showMessageDialog(null, "¡" + marker + " gana!");
                resetGame();
                return;
            }
        }
        
        boolean isTie = true;
        for (String cell : board) {
            if (cell.isEmpty()) {
                isTie = false;
                break;
            }
        }

        if (isTie) {
            JOptionPane.showMessageDialog(null, "¡Empate!");
            resetGame();
        }
    }
    
